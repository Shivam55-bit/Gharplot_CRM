import mongoose from "mongoose";

const uspCategorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
      trim: true,
      default: "",
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const USPCategory = mongoose.model("USPCategory", uspCategorySchema);

export default USPCategory;
